using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DesafioCursoCsharpOperacoes.Operacoes
{
    public class Porcentagem
    {
        public double Porcento(double valor1, double valor2) // cria um método com dois parâmetros, valor1 e valor2
        {

            return (valor1 * valor2) / 100;  // calcula a porcentagem e retorna o resultado da operação

        }
    }
}
